print(f'The result of addition between {first_n} and {second_n} is {first_n - second_n}')
# output the result of multiplication
print(f'The result of addition between {first_n} and {second_n} is {first_n * second_n}')
# output the result of division
print(f'The result of addition between {first_n} and {second_n} is {first_n / second_n}')

